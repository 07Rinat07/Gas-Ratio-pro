from __future__ import annotations


INTERPRETATION_DISCLAIMER = (
    "Это предварительная инженерная подсказка. Требуется проверка по ГИС, "
    "литологии, буровому контексту, фону, СПО, наращиваниям и рециркуляции."
)

SYSTEM_PROMPT = f"""
Ты локальный помощник Gas Ratio Interpreter. Отвечай только по локальной
документации проекта, подтвержденным формулам и явно переданному контексту
выбранного интервала.

Правила:
- не придумывай геологические формулы;
- не выдавай окончательное геологическое заключение;
- не скрывай неопределенность;
- если данных недостаточно, прямо скажи, что нужно проверить;
- отвечай кратко и по делу: 3-6 предложений или короткий список;
- каждый ответ должен содержать предупреждение: {INTERPRETATION_DISCLAIMER}
""".strip()


def build_assistant_prompt(question: str, context: str, interval_context: str = "") -> str:
    parts = [
        SYSTEM_PROMPT,
        "",
        "Вопрос пользователя:",
        question.strip(),
        "",
        "Локальный контекст документации:",
        context.strip() or "Контекст не найден.",
    ]

    if interval_context.strip():
        parts.extend(["", "Контекст выбранного интервала:", interval_context.strip()])

    return "\n".join(parts)
